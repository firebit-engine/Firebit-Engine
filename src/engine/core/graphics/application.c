// Создать приложение:
FECG_Application *FECG_CreateApplication(FECG_AppConfig *config) {
    FECG_Application *app = (FECG_Application*)calloc(1, sizeof(FECG_Application));
    if (!app) { return null; }

    _FECG_AppVars *appvars = (_FECG_AppVars*)calloc(1, sizeof(_FECG_AppVars));
    if (!appvars) { free(app); return null; }

    app->config = config;
    app->_appvars = appvars;
    return app;
}


// Освободить память приложения:
void FECG_DestroyApplication(FECG_Application *application) {
    if (!application) return;
    if (application->destroy) { application->destroy(application); }
    if (application->close)   { application->close(application); }
    if (application->_appvars) { free(application->_appvars); }
    free(application);
    return;
}


